from pyBSDate.DateConverter import _bs_to_ad, _ad_to_bs

__author__ = 'sushil'
import unittest


class TestDateConversion(unittest.TestCase):
    def test_conversion_to_ad(self):
        self.assertEqual(_bs_to_ad(2072, 8, 22), "2015-12-08")
        self.assertEqual(_bs_to_ad(2073, 4, 9), "2016-07-24")

        self.assertEqual(_bs_to_ad(2072, 1, 28), "2015-05-11")
        self.assertEqual(_bs_to_ad(2072, 2, 28), "2015-06-11")
        self.assertEqual(_bs_to_ad(2072, 3, 28), "2015-07-13")
        self.assertEqual(_bs_to_ad(2072, 4, 28), "2015-08-13")
        self.assertEqual(_bs_to_ad(2072, 5, 28), "2015-09-14")
        self.assertEqual(_bs_to_ad(2072, 6, 28), "2015-10-15")
        self.assertEqual(_bs_to_ad(2072, 7, 28), "2015-11-14")
        self.assertEqual(_bs_to_ad(2072, 8, 28), "2015-12-14")
        self.assertEqual(_bs_to_ad(2072, 9, 28), "2016-01-12")
        self.assertEqual(_bs_to_ad(2072, 10, 28), "2016-02-11")
        self.assertEqual(_bs_to_ad(2072, 11, 28), "2016-03-11")
        self.assertEqual(_bs_to_ad(2072, 12, 28), "2016-04-10")

        self.assertEqual(_bs_to_ad(2073, 1, 28), "2016-05-10")
        self.assertEqual(_bs_to_ad(2073, 2, 28), "2016-06-10")
        self.assertEqual(_bs_to_ad(2073, 3, 28), "2016-07-12")
        self.assertEqual(_bs_to_ad(2073, 4, 28), "2016-08-12")
        self.assertEqual(_bs_to_ad(2073, 5, 28), "2016-09-13")
        self.assertEqual(_bs_to_ad(2073, 6, 28), "2016-10-14")
        self.assertEqual(_bs_to_ad(2073, 7, 28), "2016-11-13")
        self.assertEqual(_bs_to_ad(2073, 8, 28), "2016-12-13")
        self.assertEqual(_bs_to_ad(2073, 9, 28), "2017-01-12")
        self.assertEqual(_bs_to_ad(2073, 10, 28), "2017-02-10")
        self.assertEqual(_bs_to_ad(2073, 11, 28), "2017-03-11")
        self.assertEqual(_bs_to_ad(2073, 12, 28), "2017-04-10")

        self.assertEqual(_bs_to_ad(2071, 1, 28), "2014-05-11")
        self.assertEqual(_bs_to_ad(2071, 2, 28), "2014-06-11")
        self.assertEqual(_bs_to_ad(2071, 3, 28), "2014-07-12")
        self.assertEqual(_bs_to_ad(2071, 4, 28), "2014-08-13")
        self.assertEqual(_bs_to_ad(2071, 5, 28), "2014-09-13")
        self.assertEqual(_bs_to_ad(2071, 6, 28), "2014-10-14")
        self.assertEqual(_bs_to_ad(2071, 7, 28), "2014-11-14")
        self.assertEqual(_bs_to_ad(2071, 8, 28), "2014-12-14")
        self.assertEqual(_bs_to_ad(2071, 9, 28), "2015-01-12")
        self.assertEqual(_bs_to_ad(2071, 10, 28), "2015-02-11")
        self.assertEqual(_bs_to_ad(2071, 11, 28), "2015-03-12")
        self.assertEqual(_bs_to_ad(2071, 12, 28), "2015-04-11")

    def test_conversion_to_bs(self):
        self.assertEqual(_ad_to_bs(2016, 1, 28), "2072-10-14")
        self.assertEqual(_ad_to_bs(2016, 2, 28), "2072-11-16")
        self.assertEqual(_ad_to_bs(2016, 3, 28), "2072-12-15")
        self.assertEqual(_ad_to_bs(2016, 4, 28), "2073-01-16")
        self.assertEqual(_ad_to_bs(2016, 5, 28), "2073-02-15")
        self.assertEqual(_ad_to_bs(2016, 6, 28), "2073-03-14")
        self.assertEqual(_ad_to_bs(2016, 7, 28), "2073-04-13")
        self.assertEqual(_ad_to_bs(2016, 8, 28), "2073-05-12")
        self.assertEqual(_ad_to_bs(2016, 9, 28), "2073-06-12")
        self.assertEqual(_ad_to_bs(2016, 10, 28), "2073-07-12")
        self.assertEqual(_ad_to_bs(2016, 11, 28), "2073-08-13")
        self.assertEqual(_ad_to_bs(2016, 12, 28), "2073-09-13")

        self.assertEqual(_ad_to_bs(2021, 1, 15), "2077-10-02")
        self.assertEqual(_ad_to_bs(2021, 2, 15), "2077-11-03")
        self.assertEqual(_ad_to_bs(2021, 3, 15), "2077-12-02")
        self.assertEqual(_ad_to_bs(2021, 4, 15), "2078-01-02")
        self.assertEqual(_ad_to_bs(2021, 5, 15), "2078-02-01")
        self.assertEqual(_ad_to_bs(2021, 6, 15), "2078-03-01")
        self.assertEqual(_ad_to_bs(2021, 7, 15), "2078-03-31")
        self.assertEqual(_ad_to_bs(2021, 8, 15), "2078-04-31")
        self.assertEqual(_ad_to_bs(2021, 9, 15), "2078-05-30")
        self.assertEqual(_ad_to_bs(2021, 10, 15), "2078-06-29")
        self.assertEqual(_ad_to_bs(2021, 11, 15), "2078-07-29")
        self.assertEqual(_ad_to_bs(2021, 12, 15), "2078-08-29")